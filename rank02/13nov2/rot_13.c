#include <unistd.h>

int	main(int argc, char *argv[])
{
	char	*s;

	if (argc != 2)
		return (write(1, "\n", 1));
	s = argv[1];
	while(*s)
	{
		
		if (*s >= 97 && *s <= 122)
		{			
			if(*s + 13 > 122)			 
				*s = 97 + (*s + 13) - 123;
			else 
				*s += 13;		
		}
		else	if (*s >= 65 && *s <= 90)
		{			
			if(*s + 13 > 90)			 
				*s = 65 + (*s + 13) - 91;
			else 
				*s += 13;		
		}	
		write(1, s, 1);
		s++;	
	}
	write(1, "\n", 1);
	return (0);
}