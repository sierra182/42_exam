#include <unistd.h>

int	main(int argc, char * argv[])
{
	char	*s;
	
	if (argc != 2)
		return (write(1, "\n", 1));
	s = argv[1];
	while (*s)
	{
		if (*s >= 97 & *s <=122)
			*s = 122 - (*s - 97);
		else if (*s >= 65 && *s <= 90)
			*s = 90 - (*s - 65);
		write(1, s++, 1);		
	}		
	write(1, "\n", 1);
	return (0);
}