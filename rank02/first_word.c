#include <unistd.h>

int	main(int argc, char *argv[])
{
	char	*s;
			
	if (argc != 2 || !*argv[1])		
		return (write(1, "\n", 1));
	s = argv[1];
	while (*s == 9 || *s == 32)
		s++;	
	while (*s && (*s != 9 && *s != 32))
		write(1, s++, 1);		
	write(1, "\n", 1);
	return (0);
}